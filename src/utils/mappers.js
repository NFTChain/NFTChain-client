export const mapExperience = experience => {
  switch (experience) {
    default:
      return '';
    case 1:
      return 'Junior developer';
    case 2:
      return 'Mid-level developer';
    case 3:
      return 'Senior developer';
    case 4:
      return 'Highly experienced developer';
    case 5:
      return 'Highly experienced developer';
    case 6:
      return 'Highly experienced developer';
  }
};
